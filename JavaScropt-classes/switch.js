var weekDay = parseInt(prompt("Enter a day : "));

switch (weekDay) {
    case 1:
        document.write("Sun Day");
        break;
    case 2:
        document.write("Mon Day");
        break;
    case 3:
        document.write("Tues Day");
        break;
    case 4:
        document.write("Wednes Day");
        break;
    case 5:
        document.write("Thurs Day");
        break;
    case 6:
        document.write("Fri Day");
        break;
    case 7:
        document.write("Satur Day");
        break;
    default:
        document.write("Invalid Day");
        break;

}